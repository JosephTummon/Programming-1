# Get input for both numbers
# Add numbers together
# Check if result is greater than 100
# If greater than 100, print That is a big number!

num1 = float(input("Please enter any number: "))
num2 = float(input("Please enter a second number to add to first: "))

result = num1 + num2 
if result > 100:
    print("That is a big number!")
exit()